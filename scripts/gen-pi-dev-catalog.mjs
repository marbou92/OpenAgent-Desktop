/**
 * Generate a compact pi-dev catalog TypeScript file from
 * @mariozechner/pi-ai's models.generated.js.
 *
 * The pi-ai catalog is bundled at install time (not fetched live) and exposes
 * 31 providers + 969 models. We rewrite it into a smaller, TypeScript-safe
 * shape that can be merged with the existing models.dev catalog.
 *
 * Output: electron/providers/pi-dev-catalog.ts
 */
import * as fs from 'node:fs';
import * as path from 'node:path';

const SRC = '/tmp/pi-ai-check/package/dist/models.generated.js';
const OUT = '/home/z/my-project/OpenAgent-Desktop/electron/providers/pi-dev-catalog.ts';

// The pi-ai models.generated.js uses ESM `export const MODELS = { ... };`.
// We import it dynamically to read the live object.
const mod = await import(SRC);
const MODELS = mod.MODELS;
if (!MODELS || typeof MODELS !== 'object') {
  throw new Error('Could not load MODELS from pi-ai package');
}

/**
 * Convert pi-ai model entry to our compact pi-dev catalog entry.
 * Keep only the fields we actually use for the merged provider list.
 */
function transformModel(providerId, modelId, raw) {
  const input = Array.isArray(raw.input) ? raw.input : [];
  return {
    id: modelId,
    name: raw.name || modelId,
    api: raw.api,
    provider: providerId,
    baseUrl: raw.baseUrl || null,
    reasoning: !!raw.reasoning,
    input,
    toolCall: !!(raw.compat?.supportsToolCalls ?? true),
    structured: !!(raw.compat?.supportsStructuredOutputs ?? true),
    cost: raw.cost || { input: 0, output: 0, cacheRead: 0, cacheWrite: 0 },
    contextWindow: raw.contextWindow || null,
    maxTokens: raw.maxTokens || null,
  };
}

const providers = {};
let totalModels = 0;
for (const [providerId, models] of Object.entries(MODELS)) {
  const out = {};
  for (const [modelId, raw] of Object.entries(models)) {
    out[modelId] = transformModel(providerId, modelId, raw);
    totalModels++;
  }
  providers[providerId] = out;
}

const providerCount = Object.keys(providers).length;

// Write the catalog as a TypeScript module.
const header = `/**
 * OpenAgent-Desktop - pi.dev static catalog (auto-generated)
 *
 * Source: @mariozechner/pi-ai@0.73.1 dist/models.generated.js
 * Generated by: scripts/gen-pi-dev-catalog.mjs
 *
 * ${providerCount} providers, ${totalModels} models.
 * The pi-ai package bundles this catalog at install time (it is NOT a live
 * API) — pi.dev itself is just a documentation site that mirrors the same
 * data. We embed the catalog directly so the user can pick "pi.dev" as a
 * catalog source without depending on the @mariozechner/pi-ai npm package
 * at runtime.
 *
 * Shape:
 *   PI_DEV_CATALOG[providerId][modelId] = {
 *     id, name, api, provider, baseUrl?, reasoning,
 *     input: ("text"|"image"|"audio"|"video"|"pdf")[],
 *     toolCall, structured,
 *     cost: { input, output, cacheRead, cacheWrite },
 *     contextWindow?, maxTokens?
 *   }
 *
 * DO NOT EDIT BY HAND — regenerate via:
 *   node /home/z/my-project/scripts/gen-pi-dev-catalog.mjs
 */

export interface PiDevCost {
  input: number;
  output: number;
  cacheRead: number;
  cacheWrite: number;
}

export interface PiDevModel {
  id: string;
  name: string;
  api: string;
  provider: string;
  baseUrl: string | null;
  reasoning: boolean;
  input: string[];
  toolCall: boolean;
  structured: boolean;
  cost: PiDevCost;
  contextWindow: number | null;
  maxTokens: number | null;
}

export type PiDevProvider = Record<string, PiDevModel>;

export const PI_DEV_CATALOG: Record<string, PiDevProvider> = `;

const body = JSON.stringify(providers, null, 2) + ';\n';

// Use a default export as well for convenience.
const footer = `
export default PI_DEV_CATALOG;

/** List all provider IDs in the pi.dev catalog. */
export function getPiDevProviderIds(): string[] {
  return Object.keys(PI_DEV_CATALOG);
}

/** Get all models for a single pi.dev provider. */
export function getPiDevModels(providerId: string): PiDevModel[] {
  const provider = PI_DEV_CATALOG[providerId];
  if (!provider) return [];
  return Object.values(provider);
}

/** Total model count across all pi.dev providers. */
export function getPiDevTotalModelCount(): number {
  let count = 0;
  for (const provider of Object.values(PI_DEV_CATALOG)) {
    count += Object.keys(provider).length;
  }
  return count;
}
`;

fs.writeFileSync(OUT, header + body + footer, 'utf-8');
console.log(`Wrote ${OUT}`);
console.log(`  Providers: ${providerCount}`);
console.log(`  Models:    ${totalModels}`);
console.log(`  Size:      ${(fs.statSync(OUT).size / 1024).toFixed(1)} KB`);
